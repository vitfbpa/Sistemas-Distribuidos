    private void jButton_FrutaKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jButton_FrutaKeyPressed
        switch (evt.getKeyCode()) {
            case 38:
                new Thread (){
                    @Override
                    public void run(){
                         Movimenta.cima(jButton_Ghost1);
                    }
                }.start();
                
                break;
               
            case 40:
                new Thread () {
                    @Override
                    public void run() {
                        Movimenta.baixo(jButton_Ghost1, JFrame_Principal.this);
                }
            }.start();
                break;
            case 37:
                Movimenta.esquerda(jButton_Ghost1);
                break;
            case 39:
                Movimenta.direita(jButton_Ghost1, this);
                break;
            default:
                throw new AssertionError();
            case 87:
                new Thread () {
                    @Override
                    public void run() {
                       Movimenta.cima(jButton_Ghost2); 
                    }
                }.start();
                break;
            case 83:
                Movimenta.baixo(jButton_Ghost2, this);
                break;
            case 65:
                Movimenta.esquerda(jButton_Ghost2);
                break;
            case 68:
                Movimenta.direita(jButton_Ghost2, this);
                break;
        }
        
        if (jButton_Ghost1.getBounds().intersects(jButton_Fruta.getBounds())) {
            Movimenta.sorteiaPosicao(jButton_Fruta, this);
        }
        if (jButton_Ghost2.getBounds().intersects(jButton_Fruta.getBounds())) {
            Movimenta.sorteiaPosicao(jButton_Fruta, this);
        }
    }//GEN-LAST:event_jButton_FrutaKeyPressed

